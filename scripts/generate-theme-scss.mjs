/* eslint-env node */

/**
 * Generates the two committed theme artifacts imported by `styles/styles.scss`:
 *
 *   - `styles/themes/default.generated.css` — the default light/dark theme, copied
 *     verbatim from `@gravity-ui/uikit-themer`'s prebuilt `styles/default.css`.
 *     Committing it (rather than importing from node_modules at build time) means a
 *     themer version bump surfaces as a reviewable token diff, and CI's empty-diff
 *     check catches it.
 *   - `styles/themes/hc.generated.css` — the high-contrast (HC) theme. themer has no
 *     HC concept (`Theme = 'light' | 'dark'`), so HC stays a repo-owned artifact: its
 *     seed lives in `styles/themes/theme-data/hc.ts` (higher-contrast palette + utility
 *     mappings) and is merged onto `DEFAULT_THEME` (whose typography/borders HC reuses)
 *     here. `generateCSS` emits `.g-root` / `.g-root_theme_light` / `.g-root_theme_dark`;
 *     we keep only the two color blocks and relabel them onto the `-hc` selectors (the
 *     common border-radius/typography block is dropped — HC reuses it from the default).
 *
 * Both outputs are COMMITTED. Regenerate with `npm run generate:theme` (run through
 * `tsx`, since the HC seed is a TypeScript module); CI fails if the working tree diff
 * is non-empty.
 */
import {readFileSync, writeFileSync} from 'node:fs';
import {createRequire} from 'node:module';
import {dirname, resolve} from 'node:path';
import {fileURLToPath} from 'node:url';

import {DEFAULT_THEME, generateCSS} from '@gravity-ui/uikit-themer';

import {hcTheme as highContrastSeed} from '../styles/themes/theme-data/hc.ts';

const require = createRequire(import.meta.url);
const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');

const defaultThemeOutputPath = resolve(projectRoot, 'styles/themes/default.generated.css');
const highContrastOutputPath = resolve(projectRoot, 'styles/themes/hc.generated.css');
const defaultThemeSourcePath = require.resolve('@gravity-ui/uikit-themer/styles/default.css');

/**
 * Normalize color literals to uikit-themer's space syntax so HC matches the shipped
 * `default.css` (raw `generateCSS` output is comma-syntax):
 *   rgb(a, b, c)      -> rgb(a b c)
 *   rgba(a, b, c, d)  -> rgb(a b c / d)
 * Already-space-syntax values are left unchanged.
 * @param css
 */
function normalizeColorsToSpaceSyntax(css) {
    return css.replace(/rgba?\(([^)]+)\)/gi, (_, inner) => {
        const channels = inner.split(',').map((channel) => channel.trim());
        const [red, green, blue, alpha] = channels;

        if (channels.length === 4) {
            return `rgb(${red} ${green} ${blue} / ${alpha})`;
        }
        if (channels.length === 3) {
            return `rgb(${red} ${green} ${blue})`;
        }
        return `rgb(${inner.trim()})`;
    });
}

/**
 * Build the "do not edit" banner prepended to every generated file.
 * @param sourceDescription
 */
function buildGeneratedFileHeader(sourceDescription) {
    return [
        '/* stylelint-disable */',
        '/*',
        ' * GENERATED by scripts/generate-theme-scss.mjs — DO NOT EDIT.',
        ` * Source: ${sourceDescription}`,
        ' * Regenerate with `npm run generate:theme`.',
        ' */',
        '',
        '',
    ].join('\n');
}

// Default light/dark theme — copied verbatim from themer's prebuilt default.css.
const defaultThemeCss = readFileSync(defaultThemeSourcePath, 'utf8').trim();
writeFileSync(
    defaultThemeOutputPath,
    buildGeneratedFileHeader('@gravity-ui/uikit-themer styles/default.css') +
        defaultThemeCss +
        '\n',
);

// HC theme: its own higher-contrast palette + utility mappings, with typography/borders
// reused from DEFAULT_THEME. generateCSS emits an already-indented `.g-root` (common)
// block followed by `.g-root_theme_light` / `.g-root_theme_dark`; relabel the two theme
// blocks onto `-hc` and slice off the common block (HC reuses it from the default theme).
const mergedHighContrastTheme = {...DEFAULT_THEME, ...highContrastSeed};
const highContrastCss = normalizeColorsToSpaceSyntax(
    generateCSS({theme: mergedHighContrastTheme, ignoreDefaultValues: false}),
)
    .replace(/\.g-root_theme_(light|dark)\b/g, '.g-root_theme_$1-hc')
    .replace(/\{\n\n+/g, '{\n'); // drop themer's blank line after each `{`

const highContrastThemeBlocks = highContrastCss
    .slice(highContrastCss.indexOf('.g-root_theme_light-hc'))
    .trimEnd();

// Guard against themer changing its output format out from under the index/slice
// logic above: both relabeled blocks must survive, otherwise we'd silently emit a
// truncated HC theme.
for (const requiredSelector of ['.g-root_theme_light-hc', '.g-root_theme_dark-hc']) {
    if (!highContrastThemeBlocks.includes(requiredSelector)) {
        throw new Error(
            `HC generation produced no \`${requiredSelector}\` block — @gravity-ui/uikit-themer's ` +
                `output format likely changed; update scripts/generate-theme-scss.mjs.`,
        );
    }
}

writeFileSync(
    highContrastOutputPath,
    buildGeneratedFileHeader(
        '@gravity-ui/uikit-themer DEFAULT_THEME + styles/themes/theme-data/hc.ts',
    ) +
        highContrastThemeBlocks +
        '\n',
);

// eslint-disable-next-line no-console
console.log(`Wrote ${defaultThemeOutputPath}\nWrote ${highContrastOutputPath}`);
